package loopingStatements.nestedPrograms;

public class pattern13 {

    public static void main(String[] args) {
        int m = 5;

        for(int i=1; i<=m;i++){
            for(int j=1; j<=m;j++){
                System.out.print(j);
            }
            System.out.println();
        }
    }
    
}
