package loopingStatements.nestedPrograms;
public class pattern12 {
    public static void main(String[] args) {
        int m = 5;

        for(int i=1; i<=m; i++){
            for(int j=1; j<=5; j++){
                System.out.print(+i);
            }
            System.out.println();
        }
    }
    
}
